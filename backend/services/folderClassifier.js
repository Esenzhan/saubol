// Определяет подпапку внутри «Анализы» правилами по ключевым словам в
// именах уже извлечённых биомаркеров — без обращения к ИИ. Ключевые слова
// откалиброваны по содержимому всех 30 реальных документов «Анализы»,
// загруженных на сайт на момент написания (см. git history этого файла).
//
// Верхнеуровневая папка документа выбирается пользователем при загрузке
// (см. TOP_FOLDERS в documents.js) — эта классификация касается только
// того, какую из 7 подпапок «Анализы» получит документ.
export const ANALYSIS_SUBFOLDERS = [
  "Общий анализ крови (ОАК)",
  "Биохимия крови",
  "Витамины и микроэлементы",
  "Иммунология / Аллергология",
  "Общий анализ мочи (ОАМ)",
  "Паразитология и инфекции",
  "Комплексные анализы",
];

const FALLBACK_FOLDER = "Комплексные анализы";

// Порядок важен: более специфичные категории проверяются раньше более
// широких, чтобы, например, "Лямблиоз IgG (КП)" не попал в "Иммунология"
// из-за подстроки "Ig", а осел в "Паразитология и инфекции".
// JS regex \b is defined via ASCII \w, so it doesn't recognize boundaries
// around Cyrillic letters at all — "\bсоэ\b" silently never matches. This
// lookaround pair is the Cyrillic-safe equivalent, used for short
// abbreviations that could otherwise false-match as a substring of an
// unrelated longer word.
const CYR_BOUNDS = (word) => `(?<![а-яё])${word}(?![а-яё])`;

const CATEGORY_RULES = [
  {
    folder: "Паразитология и инфекции",
    re: /\(кп\)|лямбли|описторх|токсокар|трихинелл|эхинококк|аскарид|анизакид|clonorchis|hbsag|гепатит/i,
  },
  {
    folder: "Общий анализ крови (ОАК)",
    re: new RegExp(
      `гемоглобин|эритроцит|тромбоцит|тромбокрит|лейкоцит|нейтрофил|эозинофил|базофил|моноцит|лимфоцит|${CYR_BOUNDS("соэ")}|цветной показатель|гематокрит|\\bmcv\\b|\\bmch\\b|\\bmchc\\b|\\brdw(-sd)?\\b|\\bmpv\\b|\\bpdw\\b|p-lcr`,
      "i"
    ),
  },
  {
    folder: "Общий анализ мочи (ОАМ)",
    re: new RegExp(`\\(моча\\)|${CYR_BOUNDS("мочи")}|уробилиноген`, "i"),
  },
  // "Витамин", "фолиевая кислота" и т.п. однозначно указывают на панель
  // микроэлементов. Голые "кальций"/"магний"/"фосфор"/"железо" намеренно
  // не входят ни в этот список, ни в "Биохимию" — эти минералы встречаются
  // в обеих панелях, и без явного вит-ключевого слова рядом их правильную
  // папку не определить надёжным правилом; пусть не голосуют ни за одну
  // категорию, а не голосуют неправильно.
  {
    folder: "Витамины и микроэлементы",
    re: /витамин|фолиевая кислота|\bb12\b|йод|ионизированный кальций|церулоплазмин/i,
  },
  {
    folder: "Иммунология / Аллергология",
    re: /иммуноглобулин/i,
  },
  // Самая широкая категория — общая биохимия крови и гормоны (отдельной
  // папки под гормоны в дереве документов нет), проверяется последней.
  {
    folder: "Биохимия крови",
    re: new RegExp(
      [
        "белок",
        "мочев",
        "креатинин",
        "глюкоза",
        "ферритин",
        CYR_BOUNDS("алт"),
        CYR_BOUNDS("аст"),
        "билирубин",
        "ггтп",
        "щелочная фосфатаза",
        CYR_BOUNDS("лдг"),
        "холестерин",
        "триглицериды",
        "амилаза",
        "калий",
        "хлориды",
        "натрий",
        "c-реактивный",
        "ревматоидный",
        "гликированный",
        CYR_BOUNDS("ттг"),
        CYR_BOUNDS("лг"),
        CYR_BOUNDS("фсг"),
        "пролактин",
        "эстрадиол",
        "тестостерон",
        CYR_BOUNDS("гспг"),
        CYR_BOUNDS("ачтв"),
        CYR_BOUNDS("мно"),
        "протромбин",
        "тромбиновое время",
      ].join("|"),
      "i"
    ),
  },
];

// Доля показателей, которую должна набрать топ-категория среди
// классифицированных, чтобы документ считался "чистой" панелью, а не
// смесью нескольких — откалибровано так, чтобы отличать один добавленный
// показатель (напр. IgE к панели ОАК) от действительно смешанного приёма
// (напр. гормоны + ОАК + вит. D в одном заборе).
const DOMINANCE_THRESHOLD = 0.7;

function categoryFor(biomarkerName) {
  for (const { folder, re } of CATEGORY_RULES) {
    if (re.test(biomarkerName)) return folder;
  }
  return null;
}

/**
 * Определяет подпапку внутри «Анализы» по списку имён извлечённых
 * биомаркеров документа. Возвращает "Комплексные анализы", если показатели
 * не распознаны ни одним правилом, поровну распределены между категориями,
 * или ни одна категория не набрала явного большинства.
 */
export function classifyAnalysisSubfolder(biomarkerNames) {
  const counts = new Map();
  for (const name of biomarkerNames) {
    const category = categoryFor(name);
    if (!category) continue;
    counts.set(category, (counts.get(category) || 0) + 1);
  }

  if (counts.size === 0) return FALLBACK_FOLDER;
  if (counts.size === 1) return [...counts.keys()][0];

  const sorted = [...counts.entries()].sort((a, b) => b[1] - a[1]);
  const [topFolder, topCount] = sorted[0];
  const [, secondCount] = sorted[1];
  if (topCount === secondCount) return FALLBACK_FOLDER;

  const total = [...counts.values()].reduce((sum, c) => sum + c, 0);
  return topCount / total >= DOMINANCE_THRESHOLD ? topFolder : FALLBACK_FOLDER;
}
